// Media Function with three parameters
function media(x, y, z) {
  return (x + y + z) / 3
}

//Using Fuction
result = media(10.0, 10.0, 9.8);
console.log(result);

//Function without parameters
function helloWorld() {
  return "Hello World!";
}

//Hello World!
console.log(helloWorld());

//Parameters without type
function square(number) {
  return number * number;
}

console.log(square(5));